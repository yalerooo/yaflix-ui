function getNestedValue(source: TranslationNode, key: string): string | undefined {
  const parts = key.split(".");
  let current: TranslationNode | undefined = source;
  for (const part of parts) {
    if (!current || typeof current === "string") return undefined;
    current = current[part];
  }
  return typeof current === "string" ? current : undefined;
}

function interpolate(template: string, params?: TranslateParams) {
  if (!params) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => {
    const value = params[key];
    return value === undefined || value === null ? "" : String(value);
  });
}

export function translate(
  language: AppLanguage,
  key: string,
  params?: TranslateParams,
  fallback?: string,
) {
  const langValue = getNestedValue(translations[language], key);
  const defaultValue =
    langValue ?? getNestedValue(translations.en, key) ?? fallback ?? key;
  return interpolate(defaultValue, params);
}
